N = int(input())

dp = [0] * 11
dp[1] = 1
dp[2] = 2
dp[3] = 4


def cal(n):
    if n <= 3:
        return dp[n]
    else:
        return cal(n - 1) + cal(n - 2) + cal(n - 3)


for x in range(N):
    answer = cal(int(input()))
    print(answer)

